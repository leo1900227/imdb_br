## 一带一路国家参与的电影的所有tt_num
tt_br <- 
  imdb_belt_road_long %>% 
  group_by(tt_num) %>% 
  summarise(n = n())



## “一带一路”沿线国家🐟独作🐟的电影的tt_num
tt_br_alone <- 
  tt_br %>% 
  filter(n == 1) %>% 
  .[1] %>% 
  as_vector()


## “一带一路”沿线国家🐟合作🐟的电影的tt_num
tt_br_col <- 
  tt_br %>% 
  filter(n != 1) %>% 
  .[1] %>% 
  as_vector()
